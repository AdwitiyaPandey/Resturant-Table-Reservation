# Camel case
print(id(firstName))  
print(id(lastName))   
print(firstName is lastName)  

# Snake case
print(id(first_name))  
print(id(last_name))   
print(first_name is last_name)  

# Pascal case
print(id(FirstName))  
print(id(LastName))   

# Flat case
print(firstname is lastname)  

# Upper flat case
print(FIRSTNAME is LASTNAME)  

# Screaming snake case
print(FIRST_NAME is LAST_NAME)  

# Pascal snake case
print(First_name is Last_name)  

print(firstName == lastName) 

